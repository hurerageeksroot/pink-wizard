## Common Mistakes (And Fixes)

1. **Assumption**: Don''t assume they remember you. Anchor to a shared event/person/outcome.
2. **Immediate pitch**: Lead with a useful asset; ask a simple question next.
3. **Generic follow‑up**: Reference your shared history or their current context.
4. **One‑and‑done**: Plan 2–3 touches spaced 5–10 days apart, each with new value.

