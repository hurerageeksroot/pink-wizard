### 2. "We can just set up our own bar"

**Response:**
```
Absolutely, you could set up your own bar—and you''d save some money upfront.

But let me ask: have you calculated the hidden costs? There''s the alcohol purchase, mixers, garnishes, ice, glassware, insurance liability if someone overserves themselves or gets injured, setup and breakdown time, and someone responsible for responsible service throughout the night.

Most importantly, as the host, do you want to spend your [holiday party/corporate event] managing the bar, or do you want to be present with your [guests/team/clients]?

We handle everything—from sourcing premium ingredients to carrying full liability insurance with professionally trained staff who know when to stop serving. You get to enjoy your own event while we create an experience your guests won''t forget.
